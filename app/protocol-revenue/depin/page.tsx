"use client";

import React, { useState } from "react";
import ChartCard from "@/app/components/shared/ChartCard";
import LegendItem from "@/app/components/shared/LegendItem";
import DePinRevenueChart from "@/app/components/charts/protocol-revenue/depin/DePinRevenueChart";
import { prepareDePinRevenueCSV } from "@/app/api/protocol-revenue/depin/depinRevenueData";
import { handleCSVDownload } from "@/app/utils/csvDownload";
import DashboardRenderer from "@/app/admin/components/dashboard-renderer";

export default function DePinPage() {
  const [modalOpen, setModalOpen] = useState(false);
  const [legends, setLegends] = useState<{label: string, color: string, value?: number}[]>([]);
  const [isDownloading, setIsDownloading] = useState(false);

  const downloadDePinRevenueCSV = async () => {
    if (isDownloading) return;
    await handleCSVDownload(
      prepareDePinRevenueCSV,
      'depin-revenue.csv',
      setIsDownloading
    );
  };

  const formatCurrency = (value?: number) => {
    if (value === undefined) return '';
    return new Intl.NumberFormat('en-US', {
      style: 'currency',
      currency: 'USD',
      notation: 'compact',
      compactDisplay: 'short',
      maximumFractionDigits: 1
    }).format(value);
  };

  return (
    <div className="space-y-6">
      
      <section className="grid grid-cols-1 gap-4">
        <ChartCard
          title="DePin Protocol Revenue"
          description="Revenue generated by DePin platforms on Solana"
          accentColor="blue"
          onExpandClick={() => setModalOpen(true)}
          onDownloadClick={downloadDePinRevenueCSV}
          isDownloading={isDownloading}
          legendWidth="1/5"
          className="h-[500px]"
          legend={
            <>
              {legends.map(legend => (
                <LegendItem
                  key={legend.label}
                  label={legend.label}
                  color={legend.color}
                  shape="square"
                  tooltipText={legend.value ? formatCurrency(legend.value) : undefined}
                />
              ))}
            </>
          }
        >
          <DePinRevenueChart
            isModalOpen={modalOpen}
            onModalClose={() => setModalOpen(false)}
            legendsChanged={setLegends}
          />
        </ChartCard>
      </section>
      
      {/* dynamic charts from admin section */}
      <DashboardRenderer pageId="depin" />
    </div>
  );
} 